%% 1.生成网格以及流动参数的设置
X=100;Y=100;%网格数，对应x向和y向，即所有物理量均存储在101*101的矩阵里进行更新
U=1;L=1;dx=L/X;dy=L/Y;%顶盖拖动速度为1，方腔的边长认为为1，原点定在左下角，对应矩阵的A(100,1)，这里存储时注意
%存储节点坐标的复数矩阵XY
for m=1:Y+1
    for n=1:X+1
XY(m,n)=dx*(n-1)+(L-dy*(m-1))*1i;
    end
end
